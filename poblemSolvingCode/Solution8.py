x = 'VARGHESE BABY'
name = (x.lower())
print(name + ' lower case')

print(name.title() + ' title case')
print(name.upper() + ' upper case')
print(name.capitalize() + ' capitalize case')
print(name.swapcase() + ' swap case')
print(name.center(30, '*') + ' center case')
print(name.ljust(30, '*') + ' left justify case')
print(name.rjust(30, '*') + ' right justify case')
print(name.zfill(30) + ' zero fill case')
print(name.lstrip() + ' left strip case')
print(name.rstrip() + ' right strip case')



'The word "Varghese" is centered in a 30-character field padded with *, then " center case" is appended.'