for i in range(1,3):
    print(i,end=" ")


"""
Step 1: Understand range(1,3)

range(start, stop) generates numbers starting from start up to but not including stop.

So, range(1,3) produces: 1, 2.

Step 2: Loop behavior

First iteration → i = 1, prints 1 with a space (because of end=" ").

Second iteration → i = 2, prints 2 with a space.

Step 3: End condition

When i would be 3, the loop stops because 3 is the exclusive limit."""